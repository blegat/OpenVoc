class Language < ActiveRecord::Base
  attr_accessible :name
  VALID_EMAIL_REGEX = /\A[A-Z][a-z]*\z/
  validates :name, presence: true, length: { maximum: 32 },
    format: { with: VALID_EMAIL_REGEX }, uniqueness: { case_sensitive: false }
end
